package com.axelsson;

public class Actor {
	//variables
	public String actorid;
	public String actorforename;
	public String actorlastname;

	public String getActorId() {
		return actorid;
	}

	public void setActorId(String actorIdSet) {
		this.actorid = actorIdSet;
	}

	public String getActorForeName() {
		return actorforename;
	}

	public void setActorForeName(String actorForeNameSet) {
		this.actorforename = actorForeNameSet;
	}

	public String getActorLastName() {
		return actorlastname;
	}

	public void setActorLastName(String actorLastNameSet) {
		this.actorlastname = actorLastNameSet;
	}
	

}
