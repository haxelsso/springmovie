package com.axelsson;

public class Movie {
	//variables
	public String movieId;
	public String name;
	public String year;

	public String getMovieId() {
		return movieId;
	}

	public void setMovieId(String movieIdSet) {
		this.movieId = movieIdSet;
	}

	public String getName() {
		return name;
	}

	public void setName(String nameSet) {
		this.name = nameSet;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String yearSet) {
		this.year = yearSet;
	}
	

}
