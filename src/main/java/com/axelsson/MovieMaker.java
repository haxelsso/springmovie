package com.axelsson;

public class MovieMaker {
	//variables
	public String makerid;
	public String makerforename;
	public String makerlastname;

	public String getMakerId() {
		return makerid;
	}

	public void setMakerId(String makerIdSet) {
		this.makerid = makerIdSet;
	}

	public String getMakerForeName() {
		return makerforename;
	}

	public void setMakerForeName(String makerForeNameSet) {
		this.makerforename = makerForeNameSet;
	}

	public String getMakerLastName() {
		return makerlastname;
	}

	public void setMakerLastName(String makerLastNameSet) {
		this.makerlastname = makerLastNameSet;
	}
	

}
